/// Report IDs matching the PTP HID descriptor.
pub const REPORTID_MULTITOUCH: u8 = 0x05;
pub const REPORTID_REPORTMODE: u8 = 0x04;
pub const REPORTID_FUNCSWITCH: u8 = 0x06;
pub const REPORTID_DEVICE_CAPS: u8 = 0x07;
pub const REPORTID_PTPHQA: u8 = 0x08;

/// Maximum number of simultaneous contacts.
pub const PTP_MAX_CONTACTS: u8 = 5;

/// Size of the touch input report (excluding the report ID byte).
///
/// Layout: 5 contacts x 9 bytes + 2 (scan time) + 1 (count) + 1 (button) = 49.
pub const TOUCH_REPORT_SIZE: usize = 49;

